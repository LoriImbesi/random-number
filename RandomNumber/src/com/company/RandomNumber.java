package com.company;

import java.util.Random;

public class RandomNumber {


    public static void main(String[] args) {


        for (int i = 0; i <= 0; i++)

        {
            //Generate a random number from 0 to 9

            int randomNum = (int) (Math.random() * 10);
            System.out.println("Here's a random number: "
                    + randomNum);

        }

        System.out.println();
    }
}
